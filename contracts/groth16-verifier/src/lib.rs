#![no_std]
use soroban_sdk::{contract, contracterror, contractimpl, contracttype, BytesN, Env, Vec};
use soroban_sdk::crypto::bn254::{Bn254G1Affine, Bn254G2Affine, Fr};

/// On-chain Groth16 verifier using Stellar's BN254 host functions (CAP-74).
#[contract]
pub struct Groth16Verifier;

#[contracttype]
#[derive(Clone)]
pub struct VerifyPublicInputsV2 {
    pub merkle_root: BytesN<32>,
    pub attestation_id: BytesN<32>,
    pub external_nullifier: BytesN<32>,
    pub nullifier_hash: BytesN<32>,
}

#[contracterror]
#[derive(Copy, Clone, Debug, Eq, PartialEq, PartialOrd, Ord)]
#[repr(u32)]
pub enum VerifierError {
    InvalidPublicSignal = 1,
    Bn128AdditionFailed = 2,
    Bn128MultiplicationFailed = 3,
    Bn128PairingFailed = 4,
}

#[contractimpl]
impl Groth16Verifier {
    pub fn verify_proof(
        env: Env,
        proof_a: BytesN<64>,
        proof_b: BytesN<128>,
        proof_c: BytesN<64>,
        pub_signals: soroban_sdk::Vec<BytesN<32>>,
    ) -> Result<bool, VerifierError> {
        if pub_signals.len() != 5 {
            return Err(VerifierError::InvalidPublicSignal);
        }
        for i in 0..5u32 {
            let signal = pub_signals.get(i).unwrap();
            if !is_valid_scalar(&signal.to_array()) {
                return Err(VerifierError::InvalidPublicSignal);
            }
        }
        let mut vk_x = VK_IC[0];
        for i in 0..5 {
            let signal = pub_signals.get(i as u32).unwrap().to_array();
            let mul_result = alt_bn128_g1_mul(&env, &VK_IC[i + 1], &signal)?;
            vk_x = alt_bn128_g1_add(&env, &vk_x, &mul_result)?;
        }
        run_pairing_check(&env, proof_a.to_array(), proof_b.to_array(), proof_c.to_array(), vk_x)
    }

    pub fn verify_proof_v2(
        env: Env,
        proof_a: BytesN<64>,
        proof_b: BytesN<128>,
        proof_c: BytesN<64>,
        public_inputs: VerifyPublicInputsV2,
    ) -> Result<bool, VerifierError> {
        let pub_signals = [
            public_inputs.merkle_root.to_array(),
            public_inputs.attestation_id.to_array(),
            public_inputs.external_nullifier.to_array(),
            public_inputs.nullifier_hash.to_array(),
        ];
        for signal in &pub_signals {
            if !is_valid_scalar(signal) {
                return Err(VerifierError::InvalidPublicSignal);
            }
        }
        let mut vk_x = VK_IC_V2[0];
        for i in 0..4 {
            let mul_result = alt_bn128_g1_mul(&env, &VK_IC_V2[i + 1], &pub_signals[i])?;
            vk_x = alt_bn128_g1_add(&env, &vk_x, &mul_result)?;
        }
        run_pairing_check(
            &env,
            proof_a.to_array(),
            proof_b.to_array(),
            proof_c.to_array(),
            vk_x,
        )
    }
}

// ---------------------------------------------------------------------------
// Shared pairing check
// ---------------------------------------------------------------------------

fn run_pairing_check(
    env: &Env,
    proof_a: [u8; 64],
    proof_b: [u8; 128],
    proof_c: [u8; 64],
    vk_x: [u8; 64],
) -> Result<bool, VerifierError> {
    let mut neg_a = proof_a;
    let a_y = &proof_a[32..64];
    let neg_a_y = field_negate(a_y);
    neg_a[32..64].copy_from_slice(&neg_a_y);

    let mut pairing_input = [0u8; 768];
    let mut offset = 0usize;
    for chunk in [
        neg_a.as_slice(),
        proof_b.as_slice(),
        VK_ALPHA.as_slice(),
        VK_BETA.as_slice(),
        vk_x.as_slice(),
        VK_GAMMA.as_slice(),
        proof_c.as_slice(),
        VK_DELTA.as_slice(),
    ] {
        pairing_input[offset..offset + chunk.len()].copy_from_slice(chunk);
        offset += chunk.len();
    }
    alt_bn128_pairing(env, &pairing_input)
}

